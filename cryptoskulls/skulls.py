import cryptoskulls.colors


class Feature(object):
    bitmap = None
    width = None
    height = None
    color_coord = None

    def __init__(self, bitmap):
        self.bitmap = bitmap
        self.width = len(bitmap[0])
        self.height = len(bitmap)

        for x in range(self.width):
            for y in range(self.height):
                if bitmap[y][x] == 2:
                    self.color_coord = (x, y)
                    break

    def present(self, im):
        assert self.width == im.width, "bitmap and image width must be same"
        assert self.height == im.height, "bitmap and image height must be same"

        feature_color = None
        for x in range(self.width):
            for y in range(self.height):
                # 0 means pixel is not part of feature
                expected = self.bitmap[y][x]
                if expected == 0:
                    continue

                # 1 means pixel is expected to be black
                if expected == 1 and im.getpixel((x, y)) != cryptoskulls.colors.K:
                    return False

                # 2 means pixel is expected to be a common color
                if expected == 2:
                    if feature_color is None:
                        feature_color = im.getpixel((x, y))
                        if im.getpixel((x, y)) != feature_color:
                            return False
        return True

    def color(self, im):
        assert self.present(im), "Cannot get color of feature that's not present"
        assert self.has_color(), "Cannot get color for colorless feature"

        return im.getpixel(self.color_coord)

    def has_color(self):
        return self.color_coord is not None


