def detect_feature(possible_features, im):
    for feature in possible_features:
        if feature.present(im):
            if feature.has_color():
                return feature.__class__.__name__, feature.color(im)
            return feature.__class__.__name__, None

    return None, None
            

class Skull(object):
    im = None
    nose = None

    def __init__(self, im):
        self.im = im

        nose, _ = detect_feature(nose_features, im)
        assert nose is not None, "Could not determine nose"
        self.nose = nose
